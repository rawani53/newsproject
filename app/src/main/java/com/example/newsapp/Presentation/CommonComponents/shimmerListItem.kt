@Composable
fun ShimmerListItem(
    isLoading: Boolean,
    contentAfterLoading: @Composable () -> Unit,
    modifier: Modifier = Modifier
) {
    if (isLoading) {
        Box(
            modifier = Modifier
                .padding(14.dp)
                .fillMaxSize()
                .clip(RoundedCornerShape(30.dp))
                .size(400.dp, 250.dp)
                .shimmerEffect()
        ) {}
    } else {
        contentAfterLoading()
    }
}

//making a custom modifier

fun Modifier.shimmerEffect(): Modifier = composed {

    //a variable to remember the size of the composable that this effect is applied to
    var size by remember { mutableStateOf(IntSize.Zero) }
    //class that creates animation
    val transition = rememberInfiniteTransition()
    // instanctiate a meathod of the class that that specifies the animation type and value
    val startOffsetX by transition.animateFloat(
        initialValue = -2 * size.width.toFloat(),
        targetValue = 2 * size.width.toFloat(),
        animationSpec = infiniteRepeatable(
            animation = tween(durationMillis = 1000)
        )
    )

    background(
        brush = Brush.linearGradient(
            colors = listOf(
                Color(0xFFB8B5B5),
                Color(0xFF8F8B8B),
                Color(0xFFB8B5B5),
            ),
            start = Offset(startOffsetX, 0f),
            end = Offset(startOffsetX + size.width.toFloat(), size.height.toFloat())
        )
    )
        .onGloballyPositioned {
            size = it.size
        }

}















